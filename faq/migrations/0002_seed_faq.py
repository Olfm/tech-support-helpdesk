def create_faq(apps, schema_editor):
    FaqItem = apps.get_model("faq", "FaqItem")
    for order, (question, answer) in enumerate(FAQ_ITEMS, start=1):
        FaqItem.objects.get_or_create(
            question=question,
            defaults={"answer": answer, "order": order, "is_published": True},
        )


